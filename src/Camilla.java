public class Camilla {
    private int idCamilla;
    private int idSala;
    private int idIsla;

    public Camilla() {
    }

    public Camilla(int idCamilla, int idSala, int idIsla) {
        this.idCamilla = idCamilla;
        this.idSala = idSala;
        this.idIsla = idIsla;
    }

    public int getIdCamilla() {
        return idCamilla;
    }

    public void setIdCamilla(int idCamilla) {
        this.idCamilla = idCamilla;
    }

    public int getIdSala() {
        return idSala;
    }

    public void setIdSala(int idSala) {
        this.idSala = idSala;
    }

    public int getIdIsla() {
        return idIsla;
    }

    public void setIdIsla(int idIsla) {
        this.idIsla = idIsla;
    }

}
